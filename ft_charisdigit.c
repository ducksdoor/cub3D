#include "cub3.h"

int	ft_charisdigit(char *nmb)
{
	int	x;

    x = 0;
	while (nmb[x])
	{
        if (nmb[x] <'0' || nmb[x] > '9')
            return (-1);
	    else
		    x++;
	}
	return (0);
}
